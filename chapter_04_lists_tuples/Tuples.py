#Tuples are just like lists but they are immutable. Its a collection of data types and its immutable.

#Empty tuple.
a = ()
print(type(a))

#If a tuple only has one item in it, we have to add a comma in front of it so that python understands its a tuple.
c = (2) #This is an int
b = (2,) #This is a tuple
print(type(b))
print(type(c))
e = ("izumi") #This is a str
d = ("izumi",) #This is a tuple
print (type(d))
print(type(e))


