#Characters that start after a \
#They comprise of more than one character but represents only 1 character when used within strings.

# \n
print("RUBY CHANNNNN\nHAIIIIIIIII\nNANI GA TSUKII\nCHOCO MINTO YORIMI ANATA")
#Used to start a new line.

#\" is read as a double quote inside a string cause obv you cant put a double quote inside a double quote.
print("\"reverse scoop\"")
print('\'straight drive\'')
print("'cover drive'")

#\\ is read as backslash inside a string.
print("this is a \\ inside a string")

#\t is for tab. 
print("Name\tAge\tCity")
print("Alice\t30\tNew York")
print("Bob\t25\tLos Angeles")

#\v is for vertical tab.
print("row 1\vrow 2")

#\a is for bell sound.
print("WARNING! \a")

