i = 1 

while i<6: #i will give the value for 1,2,3,4,5. It will stop at 6.
    print(i) 
    i += 1 #Value of i gets increased by 1.

#Flow of code:
#i will get printed.
#As while is a loop, it will go back to the loop to check if the condition is true or not.
#If its true (which in this case it is), it will undergo the function inside while loop again.

#While loop will keep getting executed until the condition is false.

